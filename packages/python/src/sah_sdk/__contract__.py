# =========================================================================
# Tier 2 — locked-spec names not yet implemented.
# =========================================================================
#
# Each guarded reference below documents a name the locked spec calls
# for but the SDK has not yet shipped. Today the ``# type: ignore``
# marker is green (the symbol is missing, mypy errors on the reference,
# the directive consumes the error). The instant the symbol lands in
# ``sah_sdk/__init__.py``, the directive's expected error disappears
# and mypy flips to red with "Unused 'type: ignore' comment." That's
# the signal to:
#
#   1. Delete the ``# type: ignore`` line.
#   2. Replace it with a real ``assert_type`` (matching Tier 1 style)
#      so the shipped signature is locked from that point forward.
#
# This guards the forward-looking #359 bug class: a future PR cannot
# land an implementation under a suffixed name (e.g. ``save_v2``)
# without the contract review catching the missing canonical.

# Future markers use ``getattr(..., "<name>", None)`` so that mypy
# does NOT flag them as missing today — instead, when the canonical
# typed accessor lands on ``CasesFacade``, the contract reviewer is
# expected to replace the ``getattr`` probe with a real typed
# assignment (e.g. ``_share: Callable[..., Any] = CasesFacade.share``).
# The named placeholder lines below are the ledger of locked-spec
# names that have not yet shipped.

from sah_sdk.zyins.cases import CasesFacade as _CasesFacade  # noqa: E402

# ``isa.zyins.cases.share`` — locked-spec §3 Flow 5. Forwarded today
# through ``CasesFacade.__getattr__`` to the legacy sub-client.
_share_placeholder: Any = getattr(_CasesFacade, "share", None)

# ``isa.zyins.cases.delete`` — locked-spec §3 Flow 5. Future
# ``DeleteableCaseStorage`` protocol extension.
_delete_placeholder: Any = getattr(_CasesFacade, "delete", None)

