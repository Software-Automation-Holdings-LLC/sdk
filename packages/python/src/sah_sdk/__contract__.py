"""Public surface contract — static-check assertions.

This file is checked by ``mypy --strict`` as part of ``hatch run lint``
(it lives under ``src/**``, included by the default mypy source-root
config). It exists purely as a guard: each typed alias / annotated
assignment asserts that a specific public symbol exists with the
locked signature. Mypy fails the moment a guarded export is renamed,
removed, or has its signature drifted.

This complements two pre-existing layers of drift detection:

1. ``packages/python/PUBLIC_API.txt`` — snapshot of every public
   symbol. Captures ANY shape change; regenerated by
   ``scripts/sdk-public-api.sh regenerate`` and diff-gated by CI
   (``.github/workflows/sdk-public-api.yml``). The primary surface guard.

2. ``packages/python/tests/test_locked_surface.py`` (where present) —
   runtime ``hasattr`` / ``isinstance`` checks documenting WHY each
   symbol is locked.

This file's contribution is the strongest tier: assertions that mypy
itself fails on at lint time, identical in spirit to the TypeScript
SDK's ``packages/ts/src/__contract__.ts`` (PR #366). The motivation is
the closed-PR #359 class of bug where a facade method snuck in under a
SUFFIXED name (e.g. ``share_v3`` instead of ``share``). The snapshot
caught the new line, but a diff that only ADDS exports is easy to wave
through; a mypy-enforced positive assertion on the canonical name
makes that mistake fail at lint time.

Maintenance: add a name here only after the locked-spec calls for it.
``# type: ignore[attr-defined]`` markers on future symbols flip from
"directive consumes an error" (green) to "Unused 'type: ignore'
comment" (red) the moment the symbol is implemented — forcing whoever
lands the implementation to remove the marker and replace it with a
real ``assert_type`` so the shipped signature is locked from that
point forward.
"""

