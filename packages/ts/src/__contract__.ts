/**
 * Public surface contract — compile-time assertions.
 *
 * This file is checked by `tsc --noEmit` (it lives under `src/**`, included
 * by `tsconfig.json`) and exists purely as a guard. It compiles to nothing
 * at runtime (no value exports). Each `type _<Name>` alias asserts that a
 * specific named export from the public barrel exists with the expected
 * shape; tsc fails the build the moment a guarded export is renamed,
 * removed, or mistyped.
 *
 * This complements two pre-existing layers of drift detection:
 *
 *   1. `packages/ts/PUBLIC_API.txt` — snapshot of every dist `.d.ts` line.
 *      Captures ANY shape change to ANY exported symbol; regenerated by
 *      `scripts/sdk-public-api.sh regenerate` and diff-gated by CI
 *      (`.github/workflows/sdk-public-api.yml`). Picks up additions
 *      automatically. The primary surface guard.
 *
 *   2. `packages/ts/tests/conformance/locked-surface.test.ts` — vitest
 *      type-level assertions citing the locked-spec section per check.
 *      Documents WHY each symbol is locked. (Note: vitest's
 *      `expectTypeOf` is loose by default — it documents intent but does
 *      not enforce until `typecheck.enabled: true` lands; this file is
 *      the tsc-strict tier.)
 *
 * This file's contribution is the strongest tier: assertions that tsc
 * actually fails on. The motivation is the closed-PR #359 class of bug
 * where a facade method snuck in under a SUFFIXED name (e.g. `shareV3`
 * instead of `share`). The snapshot caught the new line, but a diff that
 * only ADDS exports is easy to wave through; a tsc-enforced positive
 * assertion on the canonical name makes that mistake compile-fail at the
 * same instant.
 *
 * Adding to this contract: list a name only after the locked-spec calls
 * for it. The `@ts-expect-error` markers on future symbols flip from
 * "directive expected an error and got one" (green) to "directive
 * expected an error and got none" (red) the moment the symbol is
 * implemented — forcing whoever lands the implementation to remove the
 * marker and replace it with a real assertion, locking the shipped
 * signature.
 */

