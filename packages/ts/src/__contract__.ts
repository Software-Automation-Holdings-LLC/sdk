// =========================================================================
// Tier 2 — locked-spec names not yet implemented.
// =========================================================================
//
// Each `@ts-expect-error` below documents a name the locked spec calls for
// but the SDK has not yet shipped. Today the marker is green (the symbol is
// missing, tsc errors on the reference, the directive consumes the error).
// The instant the symbol lands in `src/index.ts`, the directive's expected
// error disappears and tsc flips to red with "Unused '@ts-expect-error'
// directive." That's the signal to:
//
//   1. Delete the @ts-expect-error line.
//   2. Replace it with a real `type _<Name> = ...` assertion (matching
//      Tier 1 style) so the shipped signature is locked from that point
//      forward.
//
// This is the forward-looking guard against the #359 class of bug: a
// future PR cannot land an implementation under a suffixed name (e.g.
// `saveV2`) without the contract review catching the missing canonical.

// `BundledApiVersions` — locked-spec §2.7 (per-surface map of bundled
// versions; see PR #360 docs lock).
