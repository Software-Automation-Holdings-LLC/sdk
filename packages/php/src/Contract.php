<?php

declare(strict_types=1);

/**
 * Public-surface contract guard for the PHP SDK.
 *
 * This file is checked by ``vendor/bin/phpstan analyse`` at level: max
 * via ``composer lint``. It exists purely as a guard: each typed
 * property, return-type annotation, and ``class_exists`` /
 * ``method_exists`` probe asserts that a specific public symbol
 * exists with the locked signature. PHPStan fails the moment a
 * guarded export is renamed, removed, or has its signature drifted.
 *
 * This complements two pre-existing layers of drift detection:
 *
 *   1. packages/php/PUBLIC_API.txt — snapshot of every public symbol.
 *      Captures ANY shape change; regenerated by
 *      ``scripts/sdk-public-api.sh regenerate`` and diff-gated by CI
 *      (.github/workflows/sdk-public-api.yml). The primary surface
 *      guard.
 *   2. The mirrored TypeScript contract at
 *      packages/ts/src/__contract__.ts (PR #366), which originally
 *      introduced this pattern.
 *
 * This file is the strongest tier: it makes the contract a lint
 * dependency. A rename never reaches main without Contract.php
 * re-pointing or PHPStan failing under composer lint.
 */

