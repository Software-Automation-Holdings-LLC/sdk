func genSignEvents() {
	sources := []string{"isa-platform/shared/go/events/registry.go"}
	text, ok := tryText(filepath.Join(platRepo, "shared/go/events/registry.go"))
	if !ok {
		gaps = append(gaps, "registry.go missing")
		writeFile("sign_events.go", header(sources)+"type SignEvent string\nvar signEventLabels = map[SignEvent]string{}\ntype signEventsAPI struct{}\nvar SignEvents signEventsAPI\nfunc (signEventsAPI) Label(e SignEvent) string { return signEventLabels[e] }\n")
		return
	}
	re := regexp.MustCompile(`EventType[A-Za-z0-9]+\s+EventType\s*=\s*"([^"]+)"`)
	type ev struct{ Enum, Wire string }
	out := []ev{}
	for _, m := range re.FindAllStringSubmatch(text, -1) {
		w := m[1]
		if !strings.HasPrefix(w, "document.") && !strings.HasPrefix(w, "signer.") {
			continue
		}
		var en strings.Builder
		for _, p := range strings.Split(w, ".") {
			en.WriteString(pascal(p))
		}
		out = append(out, ev{Enum: en.String(), Wire: w})
	}
	sort.Slice(out, func(i, j int) bool { return out[i].Wire < out[j].Wire })
	var b strings.Builder
	b.WriteString(header(sources))
	b.WriteString("// SignEvent is the EventBridge detail-type for RapidSign webhooks.\ntype SignEvent string\n\n")
	if len(out) > 0 {
		b.WriteString("const (\n")
		for _, e := range out {
			fmt.Fprintf(&b, "\tSignEvent%s SignEvent = %q\n", e.Enum, e.Wire)
		}
		b.WriteString(")\n\n")
	}
	b.WriteString("var signEventLabels = map[SignEvent]string{\n")
	for _, e := range out {
		fmt.Fprintf(&b, "\t%q: %q,\n", e.Wire, e.Enum)
	}
	b.WriteString("}\n\ntype signEventsAPI struct{}\n\n// SignEvents is the catalog accessor.\nvar SignEvents signEventsAPI\n\n// Label returns the PascalCase identifier for e.\nfunc (signEventsAPI) Label(e SignEvent) string { return signEventLabels[e] }\n")
	writeFile("sign_events.go", b.String())
}

