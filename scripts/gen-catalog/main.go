func header(sources []string) string {
	var b strings.Builder
	b.WriteString("// Code generated by scripts/gen_catalog.py. Regenerate with that script.\n//\n")
	b.WriteString("// Source data:\n")
	for _, s := range sources {
		b.WriteString("//   - " + s + "\n")
	}
	b.WriteString("\npackage catalog\n\n")
	return b.String()
}

func writeFile(name, content string) {
	p := filepath.Join(outDir, name)
	if err := os.WriteFile(p, []byte(content), 0o644); err != nil {
		panic(err)
	}
	fmt.Fprintln(os.Stderr, "wrote", p)
}

func pascal(s string) string {
	re := regexp.MustCompile(`[^a-zA-Z0-9]+`)
	parts := re.Split(s, -1)
	var b strings.Builder
	for _, p := range parts {
		if p == "" {
			continue
		}
		b.WriteString(strings.ToUpper(p[:1]))
		if len(p) > 1 {
			b.WriteString(strings.ToLower(p[1:]))
		}
	}
	id := b.String()
	if id != "" && id[0] >= '0' && id[0] <= '9' {
		id = "X" + id
	}
	return id
}

func slugify(s string) string {
	out := strings.ToLower(s)
	out = regexp.MustCompile(`[^a-z0-9]+`).ReplaceAllString(out, "-")
	return strings.Trim(out, "-")
}

func tryJSON(p string, dst any) bool {
	f, err := os.ReadFile(p)
	if err != nil {
		return false
	}
	if err := json.Unmarshal(f, dst); err != nil {
		fmt.Fprintln(os.Stderr, "parse", p, err)
		return false
	}
	return true
}

func tryText(p string) (string, bool) {
	f, err := os.ReadFile(p)
	if err != nil {
		return "", false
	}
	return string(f), true
}

func quoted(in []string) string {
	if len(in) == 0 {
		return "nil"
	}
	parts := make([]string, len(in))
	for i, s := range in {
		parts[i] = fmt.Sprintf("%q", s)
	}
	return "[]string{" + strings.Join(parts, ", ") + "}"
}

func productSlice(in []string) string {
	if len(in) == 0 {
		return "nil"
	}
	parts := make([]string, len(in))
	for i, s := range in {
		parts[i] = fmt.Sprintf("%q", s)
	}
	return "[]Product{" + strings.Join(parts, ", ") + "}"
}

