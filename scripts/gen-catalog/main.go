type entry struct{ Enum, Wire, Doc string }

func parseScopeEnum(body string) []entry {
	out := []entry{}
	lines := strings.Split(body, "\n")
	var pb strings.Builder
	for _, raw := range lines {
		t := strings.TrimSpace(raw)
		if strings.HasPrefix(t, "//") {
			pb.WriteString(" ")
			pb.WriteString(strings.TrimSpace(strings.TrimPrefix(t, "//")))
			continue
		}
		m := regexp.MustCompile(`^(SCOPE_[A-Z0-9_]+)\s*=\s*\d+;`).FindStringSubmatch(t)
		if len(m) == 2 {
			if m[1] == "SCOPE_UNSPECIFIED" {
				pb.Reset()
				continue
			}
			pending := pb.String()
			wm := regexp.MustCompile("`([^`]+)`").FindStringSubmatch(pending)
			if len(wm) < 2 {
				pb.Reset()
				continue
			}
			wire := wm[1]
			doc := strings.TrimSpace(regexp.MustCompile("`[^`]+`\\s*—?\\s*").ReplaceAllString(pending, ""))
			out = append(out, entry{Enum: pascal(strings.ReplaceAll(wire, ":", " ")), Wire: wire, Doc: doc})
			pb.Reset()
		} else if t == "" {
			pb.Reset()
		}
	}
	return out
}

func buildScopes(text string, sources []string) string {
	m := regexp.MustCompile(`(?ms)enum Scope \{(.*?)\}\s*$`).FindStringSubmatch(text)
	scopes := []entry{}
	if len(m) == 2 {
		scopes = parseScopeEnum(m[1])
	}
	sort.Slice(scopes, func(i, j int) bool { return scopes[i].Wire < scopes[j].Wire })
	var b strings.Builder
	b.WriteString(header(sources))
	b.WriteString("// Scope is the bearer-token scope identifier.\ntype Scope string\n\n")
	if len(scopes) > 0 {
		b.WriteString("const (\n")
		for _, s := range scopes {
			fmt.Fprintf(&b, "\t// %s\n\tScope%s Scope = %q\n", s.Doc, s.Enum, s.Wire)
		}
		b.WriteString(")\n\n")
	}
	b.WriteString("var scopeDescriptions = map[Scope]string{\n")
	for _, s := range scopes {
		fmt.Fprintf(&b, "\t%q: %q,\n", s.Wire, s.Doc)
	}
	b.WriteString("}\n\ntype scopesAPI struct{}\n\n// Scopes is the catalog accessor.\nvar Scopes scopesAPI\n\n// Description returns the documentation prose for s.\nfunc (scopesAPI) Description(s Scope) string { return scopeDescriptions[s] }\n")
	return b.String()
}

func parseErrorEnum(body string) []entry {
	out := []entry{}
	lines := strings.Split(body, "\n")
	var pb strings.Builder
	for _, raw := range lines {
		t := strings.TrimSpace(raw)
		if strings.HasPrefix(t, "//") {
			pb.WriteString(" ")
			pb.WriteString(strings.TrimSpace(strings.TrimPrefix(t, "//")))
			continue
		}
		m := regexp.MustCompile(`^ERROR_CODE_([A-Z0-9_]+)\s*=\s*\d+;`).FindStringSubmatch(t)
		if len(m) == 2 {
			if m[1] == "UNSPECIFIED" {
				pb.Reset()
				continue
			}
			out = append(out, entry{Enum: pascal(m[1]), Wire: strings.ToLower(m[1]), Doc: strings.TrimSpace(pb.String())})
			pb.Reset()
		} else if t == "" {
			pb.Reset()
		}
	}
	return out
}

func buildErrors(text string, sources []string) string {
	m := regexp.MustCompile(`(?ms)enum ErrorCode \{(.*?)\}`).FindStringSubmatch(text)
	codes := []entry{}
	if len(m) == 2 {
		codes = parseErrorEnum(m[1])
	}
	sort.Slice(codes, func(i, j int) bool { return codes[i].Wire < codes[j].Wire })
	advice := map[string]string{
		"validation_error": "fix_request_body", "idempotency_conflict": "retry_with_new_key",
		"rate_limit_exceeded": "wait_and_retry", "token_expired": "refresh_session",
		"invalid_token": "reissue_session", "license_locked": "contact_support",
		"forbidden": "check_scopes", "not_found": "verify_resource_id",
		"method_not_allowed": "check_http_method", "conflict": "reconcile_state",
		"unauthorized": "authenticate_caller", "internal_error": "retry_or_contact_support",
		"bad_gateway": "retry_with_backoff", "gateway_timeout": "retry_with_backoff",
		"service_unavailable": "retry_with_backoff", "not_implemented": "check_feature_availability",
	}
	var b strings.Builder
	b.WriteString(header(sources))
	b.WriteString("// ErrorCode is the stable wire-form error identifier.\ntype ErrorCode string\n\n")
	if len(codes) > 0 {
		b.WriteString("const (\n")
		for _, c := range codes {
			fmt.Fprintf(&b, "\t// %s\n\tErrorCode%s ErrorCode = %q\n", c.Doc, c.Enum, c.Wire)
		}
		b.WriteString(")\n\n")
	}
	b.WriteString("var errorAdviceCodes = map[ErrorCode]string{\n")
	for _, c := range codes {
		a := advice[c.Wire]
		if a == "" {
			a = "see_docs"
		}
		fmt.Fprintf(&b, "\t%q: %q,\n", c.Wire, a)
	}
	b.WriteString("}\n\nvar errorDocURLs = map[ErrorCode]string{\n")
	for _, c := range codes {
		fmt.Fprintf(&b, "\t%q: \"https://docs.isaapi.com/errors/%s\",\n", c.Wire, c.Wire)
	}
	b.WriteString("}\n\ntype errorsAPI struct{}\n\n// Errors is the catalog accessor.\nvar Errors errorsAPI\n\n")
	b.WriteString("// AdviceCode returns the machine-readable next-action identifier for c.\nfunc (errorsAPI) AdviceCode(c ErrorCode) string { return errorAdviceCodes[c] }\n\n")
	b.WriteString("// DocURL returns the canonical documentation URL for c.\nfunc (errorsAPI) DocURL(c ErrorCode) string { return errorDocURLs[c] }\n")
	return b.String()
}

