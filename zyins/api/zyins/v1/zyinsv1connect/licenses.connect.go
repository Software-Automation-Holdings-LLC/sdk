// licenses.proto defines the BPP license-lifecycle surface for ZyINS
// (signup, activation, validation, lock/unlock, refresh, deactivate).
//
// Two surfaces coexist and this file covers both:
//
//   1. Legacy `/v1/licenses/*` endpoints — server-to-server, deprecated.
//      They take credentials in the request body, accept Bearer or
